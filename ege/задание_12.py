# Открываем файл для чтения
with open("12.txt") as file:
    # Считываем последовательность целых чисел из файла и создаем список
    sequence = [int(x) for x in file]

# Инициализируем минимальное число в последовательности, не кратное 15
min_non_multiple_of_15 = 1000000

for num in sequence:
    # Находим минимальное число в последовательности, не кратное 15
    if abs(num) % 15 != 0 and num < min_non_multiple_of_15:
        min_non_multiple_of_15 = num

# Инициализируем счетчик пар и максимальную сумму
pair_count = 0
max_sum = -200000

# Перебираем последовательность для поиска пар, где оба числа кратны min_non_multiple_of_15
for i in range(len(sequence) - 1):
    if abs(sequence[i]) % min_non_multiple_of_15 == 0 and abs(sequence[i + 1]) % min_non_multiple_of_15 == 0:
        pair_count += 1
        # Находим максимальную сумму таких пар
        max_sum = max(max_sum, sequence[i] + sequence[i + 1])

# Выводим количество найденных пар и максимальную из сумм элементов таких пар
print(pair_count, max_sum)
