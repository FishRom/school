a = []
for i in range(1, 100):
    s = bin(i)[2:] #перевод во 2-ую систему счисления
    s = str(s)
    if s.count('1') % 2 == 0:
        s = "1" + s[2:] + '01'
    else:
        s = "10" + s[2:] + "0"
    r = int(s, 2) #перевод в десятичную систему счисления
    if r > 48:
        a.append(i)
print(min(a))